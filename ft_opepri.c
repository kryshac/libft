#include "libft.h"

int		ft_opepri(char c)
{
	if (c == '(' || c == ')')
		return (0);
	if (c == '*' || c == '/' || c == '%')
		return (1);
	if (c == '+' || c == '-')
		return (2);
	return (0);
}
